package day17_WhileandDoWhileLoop;
//the while loop is a repeated if statement. it is the same as if statement, just in a loop until it turns out false.
public class WhileLoopCL {

    public static void main(String[] args) {

        if(true){
            System.out.println("Wooden Spoon");// 1 time
        }


        while(true){
            System.out.println("Wooden Spoon");//thousands of wooden spoons

        }
    }
}
