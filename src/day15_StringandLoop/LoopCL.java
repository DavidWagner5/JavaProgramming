package day15_StringandLoop;

public class LoopCL {
    public static void main(String[] args) {
        //forloop
        //for(initialization; condition ; iteration){
        //prevent infinite loops always

        for(int number = 1; number<7; number+=2 ){
            System.out.println(number);
        }


        System.out.println("---------------");
        for(int i = 1; i<=10; i ++){
            System.out.println("hello world");// will only print 10 times.
        }






    }
}
